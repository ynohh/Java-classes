
public class ConversaoDeUnidadeDeArea {
    public static double metrosQuadParaPesQuad(double metrosQuadrados) {
        return metrosQuadrados * 10.764;
    }

    public static double pesQuadParaCentQuad(double pesQuad) {
        return pesQuad * 929;
    }
    
    public static double milhasQuadParaAcres(double milhasQuad) {
        return milhasQuad * 640;
    }

    public static double acresParaPesQuad(double acres) {
        return acres * 43560;
    }
    
}
